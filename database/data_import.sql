 Data Import Process

 Data for this project was imported using 
MySQL Workbench Table Data Import Wizard.

Steps followed:
1. Downloaded CSV files from GitHub repository
2. Opened MySQL Workbench
3. Right-clicked on each table → Table Data Import Wizard
4. Selected corresponding CSV file
5. Mapped columns correctly
6. Completed import for all tables:
   - categories
   - competitions
   - competitors
   - complexes
   - rankings
   - venues
